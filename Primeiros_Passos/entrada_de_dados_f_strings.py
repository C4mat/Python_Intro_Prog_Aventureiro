# entrada de dados - receber dados de usuario
# input()
# exemplo em uma variavel
nome = input("digite seu nome: ")
# usando input pra receber informação digitada de um usuario

print(nome)  # exibindo o valor digitado

# tambem é possivel fazer a utilização desse valor ja digitado em outro print nesse caso depois a string colocar uma , e o nome da varivel
print("bem vindo", nome)

# tambem temos a  F string que utilizamos para colocar o nome da variavel junto com as aspas da string e aparecer na tela para o usuario

print(f"bem vindo {nome} ")  # utilizando a f String
